import UIKit

// MARK: -----------------------------------------------------------------------
// MARK: Example 1 : Reverse print a given string

func reversePrintAGivenStringUsingAppleAPI() {
    let string = "What a lovely day!!!"
    let reversedString = string.reversed()
    print("#####################################################################")
    print(string)
    print("---------------------------------------------------------------------")
    print(String(reversedString))
    print("#####################################################################")
}

